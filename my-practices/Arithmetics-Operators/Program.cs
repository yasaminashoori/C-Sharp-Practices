using System;

namespace ArithmeticsOperators
{
    class Program
    {
        static void Main(string[] args)
        {
            int number = 3;
            number += 2;
            Console.WriteLine(number);

            number = number + 6;
            Console.WriteLine(number);

            number = number / 2;
            Console.WriteLine(number);

            number++;
            Console.WriteLine(number);
            
        }
    }
}
